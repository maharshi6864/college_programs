a = 60  # Binary: 0011 1100
b = 13  # Binary: 0000 1101

# Bitwise AND
result_and = a & b  # Binary: 0000 1100 (Decimal: 12)

# Bitwise OR
result_or = a | b   # Binary: 0011 1101 (Decimal: 61)

# Bitwise XOR
result_xor = a ^ b  # Binary: 0011 0001 (Decimal: 49)

# Bitwise NOT
result_not_a = ~a   # Example: ~60 results in -61 (due to two's complement)

# Left Shift
result_left_shift = a << 2  # Binary: 1111 0000 (Decimal: 240)

# Right Shift
result_right_shift = a >> 2 # Binary: 0000 1111 (Decimal: 15)